#!/usr/bin/env node
/**
 * Generates src/lib/version.ts at build time.
 *
 * Captures the current git SHA and build timestamp into a TS file the
 * runtime imports — replaces runtime `git describe` calls in version.ts
 * (which relied on the .git directory being present at runtime, which is
 * not always true after deploys that rsync code without .git/HEAD sync).
 *
 * Output (overwritten on every run):
 *   src/lib/version.ts:
 *     export const CURRENT_SHA = '...';
 *     export const CURRENT_SHA_SHORT = '...';
 *     export const BUILD_TIME = '...';
 *
 * If git is unavailable (fresh checkout, dev box without git), falls back
 * to placeholders that the runtime treats as dev-mode.
 */

import { execFileSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..', '..');
const outPath = join(repoRoot, 'src', 'lib', 'version.ts');

function tryGit(args) {
	try {
		return execFileSync('git', args, { cwd: repoRoot, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
	} catch {
		return null;
	}
}

const sha = tryGit(['rev-parse', 'HEAD']) ?? '';
const shortSha = sha ? sha.slice(0, 7) : '';
const buildTime = new Date().toISOString();

const content = `/**
 * Generated by scripts/build/generate-version.mjs — do not edit.
 *
 * Regenerated on every \`npm run dev\` and \`npm run build\` via the
 * \`gen:version\` prebuild step. Gitignored so each environment has its
 * own values without churning git history.
 */

export const CURRENT_SHA = ${JSON.stringify(sha)};
export const CURRENT_SHA_SHORT = ${JSON.stringify(shortSha)};
export const BUILD_TIME = ${JSON.stringify(buildTime)};
`;

writeFileSync(outPath, content, 'utf8');
console.log(`[gen:version] ${outPath} → sha=${shortSha || '(none)'} buildTime=${buildTime}`);
